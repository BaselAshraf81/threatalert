/**
 * scripts/generate-messaging-sw.js
 *
 * Generates public/firebase-messaging-sw.js with your Firebase config
 * injected from environment variables. Run automatically before every
 * build and dev start via the "prebuild" / "predev" npm scripts.
 *
 * Usage:
 *   node scripts/generate-messaging-sw.js
 *
 * Requires: a populated .env.local file (copy from .env.local.example).
 */

const fs = require("fs")
const path = require("path")

// Load .env.local so this works when called directly (not via Next.js)
try {
  const envPath = path.join(__dirname, "../.env.local")
  if (fs.existsSync(envPath)) {
    const lines = fs.readFileSync(envPath, "utf8").split("\n")
    for (const line of lines) {
      const trimmed = line.trim()
      if (!trimmed || trimmed.startsWith("#")) continue
      const eqIdx = trimmed.indexOf("=")
      if (eqIdx === -1) continue
      const key = trimmed.slice(0, eqIdx).trim()
      let val = trimmed.slice(eqIdx + 1).trim()
      // Strip surrounding quotes that some editors/shells add (e.g. KEY="value" or KEY='value')
      if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
        val = val.slice(1, -1)
      }
      if (!process.env[key]) process.env[key] = val
    }
  }
} catch {
  // Env file absent in CI — rely on process.env being pre-populated
}

const required = [
  "NEXT_PUBLIC_FIREBASE_API_KEY",
  "NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN",
  "NEXT_PUBLIC_FIREBASE_PROJECT_ID",
  "NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET",
  "NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID",
  "NEXT_PUBLIC_FIREBASE_APP_ID",
]

const missing = required.filter((k) => !process.env[k])
if (missing.length) {
  console.error("⚠️  generate-messaging-sw.js: missing env vars:", missing.join(", "))
  console.error("   Copy .env.local.example → .env.local and fill in your Firebase config.")
  // Write a stub SW so the app still builds — push just won't work
  fs.writeFileSync(
    path.join(__dirname, "../public/firebase-messaging-sw.js"),
    `// Firebase config env vars not set — push notifications disabled.\n`
  )
  process.exit(0)
}

const config = {
  apiKey: process.env.NEXT_PUBLIC_FIREBASE_API_KEY,
  authDomain: process.env.NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN,
  projectId: process.env.NEXT_PUBLIC_FIREBASE_PROJECT_ID,
  storageBucket: process.env.NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET,
  messagingSenderId: process.env.NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID,
  appId: process.env.NEXT_PUBLIC_FIREBASE_APP_ID,
}

const swContent = `// AUTO-GENERATED by scripts/generate-messaging-sw.js — do not edit manually.
// Re-generated every build. This file is gitignored.

importScripts('https://www.gstatic.com/firebasejs/10.12.0/firebase-app-compat.js');
importScripts('https://www.gstatic.com/firebasejs/10.12.0/firebase-messaging-compat.js');

firebase.initializeApp(${JSON.stringify(config, null, 2)});

const messaging = firebase.messaging();

// Handle background push messages (app not in foreground)
messaging.onBackgroundMessage((payload) => {
  // Data-only messages: title/body come from payload.data (no payload.notification)
  const { title, body, incidentId, category, url } = payload.data || {};

  self.registration.showNotification(title || 'ThreatAlert', {
    body: body || 'New incident reported near you.',
    icon: '/apple-touch-icon.png',
    badge: '/favicon-32x32.png',
    vibrate: [200, 100, 200],
    data: { incidentId, category, url: url || '/' },
    actions: [
      { action: 'view', title: 'View on map' },
      { action: 'dismiss', title: 'Dismiss' },
    ],
  });
});

// Notification click: open the specific incident deep-link
self.addEventListener('notificationclick', (event) => {
  event.notification.close();
  if (event.action === 'dismiss') return;

  const url = (event.notification.data && event.notification.data.url)
    ? event.notification.data.url
    : '/';

  event.waitUntil(
    clients.matchAll({ type: 'window', includeUncontrolled: true }).then((windowClients) => {
      // Focus an existing tab if it already has the target URL open
      for (const client of windowClients) {
        if (client.url === url && 'focus' in client) return client.focus();
      }
      return clients.openWindow(url);
    })
  );
});
`

const outPath = path.join(__dirname, "../public/firebase-messaging-sw.js")
fs.writeFileSync(outPath, swContent)
console.log("✅  Generated public/firebase-messaging-sw.js")